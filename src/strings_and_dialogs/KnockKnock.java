package strings_and_dialogs;

import javax.swing.JOptionPane;

public class KnockKnock {
public static void main(String[] args) {
	JOptionPane.showMessageDialog(null, "Knock knock.");
	JOptionPane.showMessageDialog(null, "Who's there?");
	JOptionPane.showMessageDialog(null, "Broccoli.");
	JOptionPane.showMessageDialog(null, "Broccoli who?");
	JOptionPane.showMessageDialog(null, "Broccoli does not have a last name.");
}
}
